package com.Problems;

public class FinalConcept {
    public static void main(String[] args) {
        // final keyword is used to define the constant values
        final int i=10; // Constant value that cannot be changed. Will throw error if changed

        //final keyword is also used to avoid inheritance.
        //For example if the "final" is added before a class say parent class then the methods
        // in parent class cannot be used in child class
        // syntax: public final class ParentClass

        //final keyword also prevents method overriding
        // syntax: public final void methodName()



    }
}
